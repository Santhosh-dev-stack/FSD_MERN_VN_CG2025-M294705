// 11.Create a switch-case program:
// 1 → “Data Pack”, 2 → “Talktime”, 3 → “Validity Recharge”, anything
// else → “Invalid Choice”

let choice=1;

switch (choice) {
  case 1:
    console.log('Data Pack');
    break;
  case 2:
    console.log('Talktime');
    break;
  case 3:
    console.log('Validity Recharge');
    break;
  default:
    console.log('Invalid Choice');
}
